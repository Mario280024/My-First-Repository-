// Write a program that has 1 variable with random int. value.
// It checks if the value is even or not.
class CheckNum {
  public static void main(String[]Args) {
    int Num3 = 15;
    if (Num3 % 2 == 0) {
      System.out.println(Num3 + " is an even number.");
} 
    if ( Num3 % 2 == 1) {
       System.out.println(Num3 + " is an odd number.");
}
  }
}